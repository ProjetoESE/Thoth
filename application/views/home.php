<h4>Thoth</h4>
<p>Systematic reviews are a type of literature review that uses systematic methods to collect secondary data,
	critically appraise research studies, and synthesize studies. Systematic reviews formulate research questions
	that are broad or narrow in scope, and identify and synthesize studies that directly relate to the systematic
	review question.hey are designed to provide a complete, exhaustive summary of current evidence relevant to a
	research question. Systematic reviews of randomized controlled trials are key to the practice of evidence-based
	medicine, and a review of existing studies is often quicker and cheaper than embarking on a new study.
</p>
